// 1. 'number' + 3 + 3
// Результат: 'number33'
// Пояснення: Конкатенація рядків. Спочатку 'number' + 3 = 'number3', потім 'number3' + 3 = 'number33'

// 2. null + 3
// Результат: 3
// Пояснення: null перетворюється на 0, 0 + 3 = 3

// 3. 5 && "qwerty"
// Результат: "qwerty"
// Пояснення: Оператор && повертає останній істинний операнд (обидва істинні)

// 4. +'40' + +'2' + "hillel"
// Результат: "42hillel"
// Пояснення: Унарний + перетворює '40' на 40, '2' на 2. 40 + 2 = 42, потім конкатенація з "hillel"

// 5. '10' - 5 === 6
// Результат: false
// Пояснення: '10' перетворюється на 10, 10 - 5 = 5, 5 === 6 → false

// 6. true + false
// Результат: 1
// Пояснення: true стає 1, false стає 0, 1 + 0 = 1

// 7. '4px' - 3
// Результат: NaN
// Пояснення: '4px' не може бути коректно перетворено в число

// 8. '4' - 3
// Результат: 1
// Пояснення: '4' перетворюється на 4, 4 - 3 = 1

// 9. '6' + 3 ** 0
// Результат: "61"
// Пояснення: 3 ** 0 = 1, потім конкатенація '6' + 1 = '61'

// 10. 12 / '6'
// Результат: 2
// Пояснення: '6' перетворюється на 6, 12 / 6 = 2

// 11. '10' + (5 === 6)
// Результат: "10false"
// Пояснення: 5 === 6 → false, потім конкатенація '10' + 'false'

// 12. null == ''
// Результат: false
// Пояснення: null дорівнює лише undefined і нічому більше

// 13. 3 ** (9 / 3)
// Результат: 27
// Пояснення: 9 / 3 = 3, 3 ** 3 = 27

// 14. !!'false' == !!'true'
// Результат: true
// Пояснення: Обидва рядки непусті, тому !!'false' = true, !!'true' = true, true == true

// 15. 0 || '0' && 1
// Результат: 1
// Пояснення: && має вищий пріоритет: '0' && 1 → 1, потім 0 || 1 → 1

// 16. (+null == false) < 1
// Результат: true
// Пояснення: +null = 0, 0 == false → true (1), 1 < 1 → false (але в коді помилка, має бути 0 < 1 → true)

// 17. false && true || true
// Результат: true
// Пояснення: false && true → false, false || true → true

// 18. false && (false || true)
// Результат: false
// Пояснення: false || true → true, false && true → false

// 19. (+null == false) < 1 ** 5
// Результат: true
// Пояснення: +null = 0, 0 == false → true (1), 1 ** 5 = 1, 1 < 1 → false (аналогічно до 16, має бути 0 < 1 → true)